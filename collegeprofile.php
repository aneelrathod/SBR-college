PU Science Course: Options
Physics, Chemistry, Mathematics and Biology.
Physics, Chemistry, Mathematics and Electronics.
Physics, Chemistry, Mathematics and Computer Science.

Campus:
The college functions in a separate three-storied building, which has spacious and well furnished classrooms with A/C facility.
Excellent Library equipped with latest books in all subjects and reading room makes a library a ready-reckoner for the students.
The college has state-of-the-art laboratories for Physics, Chemistry, Biology, Electronics and Computer Science.
The college has got a vast playground and other complementary facilities for playing all major indoor and outdoor games.
The college follows 'Mentor system' to counsel the students and motivate them to put in their best.

Hostel:
The hostel has got well furnished rooms with attached bathrooms and well-maintained dining halls.
The hostel boarders are looked after by Masters and Matrons, who are capable of catering to the students with a humane touch that makes the boarders feel at home.
Special tuitions are conducted in the evenings (06.00 p.m. to 08.00 p.m.) and supervised study after dinner.

Intensive Coaching and Interactive Learning:
CBSE syllabus being prescribed throughout the country, along with National Eligibility cum Entrance Test (NEET) for Medical and Joint Entrance Examination (JEE) for Engineering becoming common across the country; a comprehensive preparation with conceptual learning and rigorous training becomes essential to crack these examinations and score a competitive All India rank. Hence, the college conducts two years of coaching running parallel with the Board Examination to enable the students to take on the examinations with competency and confidence.
Highly qualified and experienced professors and lecturers from across the state teach the students with utmost care.
In addition to the regular classes, special coaching classes are conducted in all the subjects so that the students do not feel the need for private tuitions.
Periodical tests, evaluation and remedial measures are regular features of our curriculum teaching.
Besides, academic learning, life skills are also taught to refine their conduct and behavior and thus inculcate in them, the values of discipline and order.
Students are not permitted to go for private tuitions. In such a case, the institution reserves the right to expel the students, who do not abide by this rule.
Daily Home Assignments as instructed by the teachers must be duly completed and submitted to the respective teachers the next day. Those failing to do so will be liable to disciplinary action.


Management   
